const STORAGE_KEY = 'shiloku-lang';

let currentLang = 'zh';

export function getLang() {
    return currentLang;
}

export function t(key) {
    const pack = STRINGS[currentLang] || STRINGS.zh;
    return pack[key] ?? STRINGS.zh[key] ?? key;
}

export function detectLang() {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && LANGS.includes(saved)) return saved;
    const nav = (navigator.language || 'zh').slice(0, 2).toLowerCase();
    if (nav === 'ja') return 'ja';
    if (nav === 'en') return 'en';
    return 'zh';
}

export function setLang(lang) {
    if (!LANGS.includes(lang)) return;
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang === 'ja' ? 'ja' : 'en';
    applyI18nDom();
    window.dispatchEvent(new CustomEvent('shiloku:langchange', { detail: { lang } }));
}

export function applyI18nDom() {
    document.querySelectorAll('[data-i18n]').forEach((el) => {
        const key = el.getAttribute('data-i18n');
        if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach((el) => {
        const key = el.getAttribute('data-i18n-title');
        if (key) el.title = t(key);
    });
    const title = document.querySelector('title');
    if (title) title.textContent = t('siteTitle');
}

export function initI18n() {
    currentLang = detectLang();
    document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : currentLang === 'ja' ? 'ja' : 'en';
    applyI18nDom();
}

/** 供 index.html 内联脚本使用的全局问候语 */
export function getGreetingForHour(hour) {
    if (hour >= 5 && hour < 11) return t('greetingMorning');
    if (hour >= 11 && hour < 14) return t('greetingNoon');
    if (hour >= 14 && hour < 18) return t('greetingAfternoon');
    if (hour >= 18 && hour < 23) return t('greetingEvening');
    return t('greetingNight');
}

export function formatDateLine(bjTime, weekDaysZh) {
    const y = bjTime.getFullYear();
    const m = bjTime.getMonth() + 1;
    const d = bjTime.getDate();
    const w = weekDaysZh[bjTime.getDay()];
    if (currentLang === 'en') {
        const enDays = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];
        return `${y}-${String(m).padStart(2, '0')}-${String(d).padStart(2, '0')} · ${enDays[bjTime.getDay()]}`;
    }
    if (currentLang === 'ja') {
        const jaDays = ['日', '月', '火', '水', '木', '金', '土'];
        return `${y}年${m}月${d}日 · ${jaDays[bjTime.getDay()]}曜日`;
    }
    return `${y}年${m}月${d}日 · 星期${w}`;
}
